let n1 = parseFloat(prompt("Digite o primeiro número"));
let n2 = parseFloat(prompt("Digite o segundo número"));
let n3 = parseFloat(prompt("Digite o terceiro número"));

let n4 = n2 + n3;

if(n1 > n4){
  alert("O primeiro número é maior do que a soma dos outros dois");
}else{
  alert("A soma do segundo número e terceiro número é maior do que o primeiro número.")
}
